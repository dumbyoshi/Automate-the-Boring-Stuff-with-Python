#in this part we are gonna see about Boolean values
print(9>10)
print(10<7)
#we get the ans as False!
#Binary Boolean Operators :
print(True and True)
print(True and False)
#The and operator evaluates an expression to True or False and then returns the last evaluated value.
print(False and False)
print(False and True)
#The NOT operator simply evaluates to the opposite Boolean value.
print(not True)
print(not False)
#The OR operator, on the other hand, is the opposite of AND. It evaluates to True if one of the expressions is True.
print(True or True)
print(True or False)
print(False or True)
print(False or False)
#Mixing Boolean and Comparison Operators
print((1<5) and (5<10))
